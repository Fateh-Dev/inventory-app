  constructor() {
    const saved = localStorage.getItem('fth-lang') as 'fr' | 'en' | 'ar';
    if (saved && ['fr', 'en', 'ar'].includes(saved)) {
      this.setLanguage(saved);
    } else {
      this.setLanguage('fr');
    }
  }

  setLanguage(lang: 'fr' | 'en' | 'ar') {
    this.currentLang.set(lang);
    localStorage.setItem('fth-lang', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }

  t(key: string): string {
    const lang = this.currentLang();
    return this.dict[lang]?.[key] ?? this.dict['fr']?.[key] ?? key;
  }
